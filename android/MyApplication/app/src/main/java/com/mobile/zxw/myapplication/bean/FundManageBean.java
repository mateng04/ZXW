package com.mobile.zxw.myapplication.bean;

public class FundManageBean {

    private String fundTime;
    private String fundMoney;
    private String fundContent;

    public String getFundTime() {
        return fundTime;
    }

    public void setFundTime(String fundTime) {
        this.fundTime = fundTime;
    }

    public String getFundMoney() {
        return fundMoney;
    }

    public void setFundMoney(String fundMoney) {
        this.fundMoney = fundMoney;
    }

    public String getFundContent() {
        return fundContent;
    }

    public void setFundContent(String fundContent) {
        this.fundContent = fundContent;
    }
}
